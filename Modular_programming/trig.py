# This impliments trigonometric functions
import math
# find the sine
def sin(x):
    angle_rad=math.radians(x)
    angle_sin =math.sin(angle_rad)
    return angle_sin

def cos(x):
    angle_rad=math.radians(x)
    angle_cos =math.cos(angle_rad)
    return angle_cos

def tan(x):
    angle_rad=math.radians(x)
    angle_tan =math.tan(angle_rad)
    return angle_tan